// Some Escape Notations:

// We use \ before ' so that it must not be mistaken with the ending note.
const statement = 'It\'s unknown.';
    console.log(statement); // Output : It's unknown.

// We use \n to introuduce a new line.
const speech = '\nMy name is Candle.\nI live in Flame.\nI have wax as well.';
    console.log(speech); // Output: Every statement will be shown in a different line.
    console.log(speech.split(''));
    console.log(speech.split(' '))
    console.log(speech.repeat(3));