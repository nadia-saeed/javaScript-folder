
// Create a program that checks whether a given number is positive, negative, or zero.

// Digit
// If digit is greater than zero: Positive
// If digit is less than zero: Negative
// Otherwise: Zero

let digit = -1

if (digit > 0){
    console.log('Positive');
}
else if (digit < 0){
    console.log('Negative');
}
else{
    console.log('Zero');
}
    

